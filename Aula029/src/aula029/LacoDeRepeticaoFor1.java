package aula029;

public class LacoDeRepeticaoFor1 {
	public static void main(String[] args) {
		//loop for 1
		
		for(int x = 0; x <= 10; ++x) {
			System.out.println(x);
			
			/*
			 * for(parte1; parte2; parte3)
			 * 
			 * parte1: é onde nós declaramos uma variável
			 * 
			 * parte2: é onde nós colocamos a condição para que o looping
			 * continue ou seja terminado
			 * 
			 * parte3: é onde nós incrementamos a nossa variável, para que dê continuidade ao nosso looping
			 */
		}
		
	}

}
