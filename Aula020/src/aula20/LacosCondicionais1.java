package aula20;

public class LacosCondicionais1 {
	public static void main(String[] args) {
		
		//Introdução aos laços condicionais|\
		//Controle de fluxo de um programa.
		//Um laço de repetição é um bloco de instrução que é repetido enquanto uma condição for verdadeira.
		
		//Atalho para executar o código no eclipse: CTRL + F5
		
		for(byte x = 0; x <= 9; x++) {
			System.out.println(x);
		}
		System.out.println("=================================");
		
		byte y = 10;
		while(y <= 20) {
			System.out.println(y);
			y += 1;
		}
	}

}
