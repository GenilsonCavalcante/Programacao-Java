package aula30;

public class LacoDeRepeticaoFor2 {
	public static void main(String[] args) {
		
		/*
		 * Em um for, temos 3 partes, e cada uma delas possuem um nome.
		 * 
		 * exemplo:
		 * 
		 * for(int i = 0; i <= 10; ++i){}
		 * 
		 * int i = 0; ---> INICIALIZAÇÃO
		 * i <= 10; ---> EXPRESSÃO
		 * ++i ---> ATUALIZAÇÃO
		 * 
		 * Sendo que cada um desses não são obrigatório
		 * a especificação, podendo então colocar um (;)
		 * envés de dexar em branco.
		 * 
		 * exemplo:
		 */
		
		for(;;) {
			//Sem especificar nada.
		}
	}

}
